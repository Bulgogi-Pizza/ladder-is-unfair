package org.seulki;

public class Gift {
    int no;
    String gift;

    public Gift(int no, String gift) {
        this.no = no;
        this.gift = gift;
    }

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public String getGift() {
        return gift;
    }

    public void setGift(String gift) {
        this.gift = gift;
    }
}
